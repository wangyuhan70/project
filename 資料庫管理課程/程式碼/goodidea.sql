SQL scripts and PHP files support a food recipe and purchasing system, featuring user management and ingredient storage tracking.

CREATE DATABASE IF NOT EXISTS GOODIDEA;

USE GOODIDEA;

CREATE TABLE IF NOT EXISTS DISH
(DISH_ID INT,
DISH_NAME VARCHAR(20),
DISH_DESCRIPTION VARCHAR(1000),
PRIMARY KEY(DISH_ID)
);

CREATE TABLE IF NOT EXISTS INGRE_CATEGORY
(INGRE_CTG_ID INT,
INGRE_CTG_NAME VARCHAR(5),
primary key(INGRE_CTG_ID)
);

CREATE TABLE IF NOT EXISTS MAINUSER
(USER_ID INT,
USER_NAME CHAR(3),
USER_PHONE_NUM CHAR(10),
USER_PASSWORD VARCHAR(20),
PRIMARY KEY(USER_ID)
);
ALTER TABLE MAINUSER ADD UNIQUE KEY USER_PHONE_NUM (USER_PHONE_NUM);

ALTER TABLE MAINUSER
DROP PRIMARY KEY,
MODIFY COLUMN USER_ID INT AUTO_INCREMENT,
ADD PRIMARY KEY (USER_ID);

ALTER TABLE MAINUSER
MODIFY COLUMN USER_NAME VARCHAR(50),
MODIFY COLUMN USER_PASSWORD VARCHAR(255);

CREATE TABLE IF NOT EXISTS INGREDIENT
(INGREDIENT_ID INT ,
INGREDIENT_CTG_ID INT NOT NULL,
INGREDIENT_NAME VARCHAR(5),
PRIMARY KEY(INGREDIENT_ID),
FOREIGN KEY(INGREDIENT_CTG_ID) REFERENCES INGRE_CATEGORY(INGRE_CTG_ID)
);



CREATE TABLE IF NOT EXISTS STORE
(USER_ID INT ,
STORE_AMOUNT VARCHAR(20),
INGREDIENT_ID INT NOT NULL,
STORE_TIME DATE,
STORE_DUR INT NOT NULL,
STORE_EXP DATE NOT NULL,
PRIMARY KEY(USER_ID,INGREDIENT_ID,STORE_TIME)
);

alter table store drop STORE_DUR;


CREATE TABLE IF NOT EXISTS DISH_INGRE
(DISH_ID INT ,
INGREDIENT_ID INT,
PRIMARY KEY(DISH_ID,INGREDIENT_ID)
);

